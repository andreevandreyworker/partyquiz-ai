def build_user_prompt(draft: str, language: str) -> str:
    lang = "русском" if language == "ru" else "английском"
    if draft.strip():
        return (
            f"Переформулируй в спорное норм/стрём-утверждение на "
            f"{lang} языке: {draft.strip()}"
        )
    return (
        f"Придумай одно спорное норм/стрём-утверждение для компании "
        f"на {lang} языке."
    )
